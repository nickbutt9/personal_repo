# This folder contains the two coding assignments and final project assigned in UWs SYDE599 - deep learning
# The first two assignments were completed in groups of 4, and the final project in a group of 5
# Contributions were shared between all group members, but knowledge was consolidated during and after completion

# The first assignment creates an autodifferentiation package using Numpy and tests its functionality using
# fully connected multi-layer perceptrons

# The second assignment uses a convolutional neural network to classify MNIST digits with Optuna for hyperparameter
# fine tuning

# The final project goal was to create any kind of model to predict the onset of freezing of gait in Parkinson's
# patients. Variations of CNNs, RNNs, and transformers were implemented and tested to compare results, with the
# optimal results being achieved by an LSTM+CNN model. The paper and data the used data was collected from can be
# found from this link: https://data.mendeley.com/datasets/r8gmbtv7w2/3
